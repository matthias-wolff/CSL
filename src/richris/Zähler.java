package richris;

public class Zähler {

  public static void main(String[]args){
    
    
    int counter=5;
    
    for (int i=counter; i != -6; i--){ // Laufzeitvariable i wird zugewiesen, läuft bis -6 (-5), wir immer um 1 (--) verringert
      
      System.out.println(i);
    }
  }
}
