  protected AHammerfallAudioDevice(int audioChannels) {
    super(DEVICE_NAME, audioChannels);

    this.channelCount = audioChannels;

    inBuffer = new float[AudioInputConstants.FRAME_SIZE * channelCount];

    outBuffer = new float[channelCount][AudioInputConstants.FRAME_SIZE];

    levelsMs = new float[channelCount];

    volumeMixer = new float[channelCount];

    Arrays.fill(volumeMixer, 1.0f);
  }
  
  /**
   * Add listener to get data from audio device RME Hammerfall.
   * @param audioListener
   */
  public void addAudioListener(IAudioListener audioListener) {

    for (IAudioListener iAudioListener : audioListeners) {

      if (iAudioListener.getName().equals(audioListener.getName()))
        return;

    }
    audioListeners.add(audioListener);
  }

  /**
   * Remove audio listener from audio device RME Hammerfall.
   * @param audioListener
   */
  public void removeAudioListener(IAudioListener audioListener) {

    for (IAudioListener iAudioListener : audioListeners) {

      if (iAudioListener.getName().equals(audioListener.getName())) {
        audioListeners.remove(iAudioListener);
        break;
      }
    }
  }
  
  /**
   * Notify all audio listeners that new audio data available.
   */
  protected void notifyListeners() {

    for (IAudioListener iAudioListener : audioListeners) {
      iAudioListener.notifyAudioEvent();
    }
  }

  /**
   * {@link APortAudioInputDevice#isInBufferZero()}
   */
  @Override
  protected boolean isInBufferZero() {
    int counter = 0;
    for (int i = 0; i < inBuffer.length; i++)
      if (inBuffer[i] == 0)
        counter++;

    if (counter == inBuffer.length)
      return true;
    return false;
  }

  /**
   * Returns the current RMS of an input channel.
   * 
   * @param channel
   *          The zero-based channel index.
   * @return The RMS, [0...1].
   */
  public float getRMS(int channel) {
    return (float) Math.sqrt(levelsMs[channel]);
  }

  /**
   * Returns the current level of an input channel.
   * 
   * @param channel
   *          The zero-based channel index.
   * @return The level, [-&infin;...0] dB.
   */
  public float getLevel(int channel) {
    return (float) (10 * Math.log10(levelsMs[channel]));
  }
  
  //Method return outbuffer.
  public float[][] getOutBuffer() {

    return outBuffer;
  }

  // Method return buffer from specific channel
  public float[] getChannelBuffer(int channelNumber) {

    return outBuffer[channelNumber];
  }

  /**
   * Convert gain from dB to linear and amplify channel.
   * @param channelNumber
   * @param gain
   */
  public void setGain(int channel, float gain) {
    
    volumeMixer[channel] = (float) Math.pow(10, (gain / 20));
  }

